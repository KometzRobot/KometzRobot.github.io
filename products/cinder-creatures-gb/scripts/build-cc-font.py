# 16-col layout: index 0..63 maps left-to-right, top-to-bottom.
LAYOUT = list(" !\"#$%&'()*+,-./0123456789:;<=>?@ABCDEFGHIJKLMNOPQRSTUVWXYZ[\\]^_")
assert len(LAYOUT) == 64

img = Image.new("RGB", (W, H), PAL[0])
pix = img.load()
for idx, ch in enumerate(LAYOUT):
    cx = (idx % COLS) * CELL
    cy = (idx // COLS) * CELL
    rows = G.get(ch)
    if not rows:
        continue
    for ry in range(8):
        row = rows[ry]
        for rx in range(8):
            if rx < len(row) and row[rx] == "#":
                pix[cx + rx, cy + ry] = PAL[3]

out_path = os.path.join(
    os.path.dirname(__file__),
    "..", "plugins", "cinder-creatures", "sprites", "cc_font_8.png",
)
img.save(out_path)
print(f"wrote {out_path} ({W}x{H}, 64 glyphs, 4-shade DMG)")


